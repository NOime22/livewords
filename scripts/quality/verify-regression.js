#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const { spawnSync } = require("child_process");

const ROOT = path.resolve(__dirname, "..", "..");
const EVIDENCE_DIR = path.join(ROOT, ".sisyphus", "evidence");
const EVIDENCE_INDEX = path.join(EVIDENCE_DIR, "task-14-evidence-index.txt");

function parseArgs(argv) {
  const args = {
    injectFailure: null,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === "--inject-failure") {
      const next = argv[i + 1];
      if (next && !next.startsWith("--")) {
        args.injectFailure = String(next);
        i += 1;
      } else {
        args.injectFailure = "lint";
      }
    }
  }

  return args;
}

function runCheck(check) {
  console.log(`[verify:regression] ${check.name}`);
  const result = spawnSync(check.command, check.args, {
    cwd: ROOT,
    stdio: "inherit",
    shell: false,
  });

  if (result.error) {
    throw new Error(`${check.id} failed to start: ${result.error.message}`);
  }

  const rc = typeof result.status === "number" ? result.status : 1;
  if (rc !== 0) {
    throw new Error(`${check.id} failed with exit code ${rc}`);
  }
}

function scenarioFromFilename(filename) {
  const base = filename.replace(/\.[^.]+$/, "");
  const parts = base.split("-");
  if (parts.length < 4) {
    return "unspecified";
  }
  return parts.slice(3).join(" ");
}

function buildEvidenceIndex() {
  fs.mkdirSync(EVIDENCE_DIR, { recursive: true });

  const entries = fs
    .readdirSync(EVIDENCE_DIR)
    .filter((name) => /^task-\d+-/.test(name) && name !== path.basename(EVIDENCE_INDEX))
    .sort((a, b) => {
      const taskA = Number((a.match(/^task-(\d+)-/) || [])[1] || 0);
      const taskB = Number((b.match(/^task-(\d+)-/) || [])[1] || 0);
      if (taskA !== taskB) return taskA - taskB;
      return a.localeCompare(b);
    });

  const lines = [
    "# Evidence Index (Task 14)",
    "",
    "This index is generated by npm run verify:regression.",
    "",
  ];

  for (const name of entries) {
    const match = name.match(/^task-(\d+)-/);
    const task = match ? `task-${match[1]}` : "task-unknown";
    const scenario = scenarioFromFilename(name);
    lines.push(`- ${task} | scenario: ${scenario} | file: .sisyphus/evidence/${name}`);
  }

  fs.writeFileSync(EVIDENCE_INDEX, `${lines.join("\n")}\n`, "utf8");
  console.log(`[verify:regression] evidence index: ${path.relative(ROOT, EVIDENCE_INDEX)}`);
}

function main() {
  const args = parseArgs(process.argv.slice(2));

  const checks = [
    { id: "verify", name: "root verify", command: "npm", args: ["run", "verify"] },
    { id: "smoke-userData-default", name: "smoke userData default selftest", command: "npm", args: ["run", "smoke:userData"] },
    { id: "smoke-userData", name: "smoke userData idempotency", command: "node", args: ["scripts/smoke/userData.js", "--case", "idempotency"] },
    { id: "smoke-storyData", name: "smoke storyData", command: "npm", args: ["run", "smoke:storyData"] },
    { id: "smoke-db-index", name: "smoke db-index", command: "npm", args: ["run", "smoke:db-index"] },
    { id: "verify-docs", name: "docs consistency validator", command: "npm", args: ["run", "verify:docs"] },
    { id: "verify-rollout", name: "rollout validator", command: "npm", args: ["run", "verify:rollout"] },
    { id: "smoke-revival-ui", name: "revival ui static check", command: "node", args: ["scripts/smoke/index-revival-ui.js", "--case", "happy"] },
    { id: "smoke-revival-flow", name: "story revival static check", command: "node", args: ["scripts/smoke/storyData.js", "--case", "revival-happy"] },
    { id: "smoke-quality-context", name: "story quality context static check", command: "node", args: ["scripts/smoke/storyData.js", "--case", "quality-context"] },
    { id: "smoke-branch-flow", name: "story branch static check", command: "node", args: ["scripts/smoke/storyData.js", "--case", "branch-happy"] },
  ];

  if (args.injectFailure) {
    const forcedMap = {
      lint: {
        id: "forced-lint-failure",
        name: "forced lint failure",
        command: "npm",
        args: ["run", "lint", "--", "--inject-failure"],
      },
      verify: {
        id: "forced-verify-failure",
        name: "forced verify failure",
        command: "npm",
        args: ["run", "lint", "--", "--inject-failure"],
      },
    };

    const forced = forcedMap[args.injectFailure];
    if (!forced) {
      const accepted = Object.keys(forcedMap).join(", ");
      throw new Error(`unsupported --inject-failure target: ${args.injectFailure}. expected: ${accepted}`);
    }
    checks.push(forced);
  }

  try {
    for (const check of checks) {
      runCheck(check);
    }
  } finally {
    buildEvidenceIndex();
  }

  console.log("[verify:regression] PASS");
}

main();
